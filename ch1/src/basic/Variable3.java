package basic;

public class Variable3 {
    public static void main(String[] args) {
        // 실수형 : float, double(기본타입)
        // float score1 = 50;
        float score1 = 50.15f;
        double score2 = 45.123d;
        System.out.println("score1 = " + score1); // 50.0
        System.out.println("score1 = " + score2); // 45.0
    }

}
